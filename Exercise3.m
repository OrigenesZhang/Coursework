%This is to plot the figures of matrices after transformation
close all;
clear all;
clc;
R(1:3,1) = [0 0 0];
R(1:3,2) = [1 0 0];
R(1:3,3) = [0 1 0];
R(1:3,4) = [0 0 1];
R(1:3,5) = [1 1 0];
R(1:3,6) = [1 0 1];
R(1:3,7) = [0 1 1];
R(1:3,8) = [1 1 1];
Edge_vertex_index=[1 2; 1 3; 1 4; 2 5; 2 6; 3 5; 3 7; 4 6; 4 7; 5 8; 6 8; 7 8];
figure_1 = figure;
plot_points_edges(figure_1 , R, Edge_vertex_index, 'g',18);
A=[1 0 0; 0 1 0; 0 0 1];
Ra=A*R;
plot_points_edges(figure_1 , Ra, Edge_vertex_index, 'b',10);
B(1,:)=A(2,:);
B(2,:)=A(1,:);
B(3,:)=A(3,:);
Rb=B*R;
figure_2 = figure;
plot_points_edges(figure_2 , R, Edge_vertex_index, 'g',18);
plot_points_edges(figure_2 , Rb, Edge_vertex_index, 'r',10);
C(1,:)=2*A(1,:);
C(2,:)=A(2,:);
C(3,:)=A(3,:);
Rc=C*R;
figure_3 = figure;
plot_points_edges(figure_3 , R, Edge_vertex_index, 'g',18);
plot_points_edges(figure_3 , Rc, Edge_vertex_index, 'k',10);
D(1,:)=A(1,:);
D(2,:)=2*A(1,:)+A(2,:);
D(3,:)=A(3,:);
Rd=D*R;
figure_4 = figure;
plot_points_edges(figure_4 , R, Edge_vertex_index, 'g',18);
plot_points_edges(figure_4 , Rd, Edge_vertex_index, 'm',10);
